export function getRegisteredUsers() {
  try {
    const raw = localStorage.getItem(USERS_STORAGE_KEY);
    if (!raw) {
      localStorage.setItem(USERS_STORAGE_KEY, JSON.stringify(SEED_USERS));
      return SEED_USERS;
    }
    return JSON.parse(raw);
  } catch (e) {
    return SEED_USERS;
  }
}

export function saveRegisteredUser(userObj) {
  try {
    const users = getRegisteredUsers();
    const existingIndex = users.findIndex(u => u.email === userObj.email);
    if (existingIndex >= 0) {
      users[existingIndex] = { ...users[existingIndex], ...userObj };
    } else {
      users.unshift({
        id: 'usr_' + Date.now(),
        registeredAt: new Date().toISOString(),
        ...userObj
      });
    }
    localStorage.setItem(USERS_STORAGE_KEY, JSON.stringify(users));
    return users;
  } catch (e) {
    return [];
  }
}

